#include <stdio.h>

int main()
{

    int num = 20;

    printf("%d", num);

    // pre increment;

    printf("\n%d", ++num);

    //post increment;

    printf("\n%d", num++);

    //latest value

    printf("\n latest value %d", num);

    //pre decrement;

    printf("\n %d", --num);

    //post decrement;

     printf("\n %d", num--);

     //latest value

     printf("\n latest value %d", num);





}