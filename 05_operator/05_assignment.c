#include <stdio.h>

int main()
{

    int num;
    num = 24;

    //add & assign

    printf("\n add & assign %d", num += 24);

    // substraction & assign

    printf("\n sub & assign %d", num -= 24);

    // multiply & assign

    printf("\n multiply & assign %d", num *= 24);

    // division & assign

    printf("\n div & assign %d", num /= 24);

    //modulo & assign

    printf("\n modulo & assign %d", num %= 24);
}