#include <stdio.h>

int main()
{

    int num1 = 10;
    int num2 = 20;

    // lessthan <;

    printf("%d", num1 < num2);

    // Greaterthan >;

    printf("\n%d", num1 > num2);

    // greater or equally

    printf("\n%d", num1 >= num2);

    // less or equally

    printf("\n%d", num1 <= num2);

    // equal

    printf("\n%d", num1 == num2);

    // not equal

    printf("\n%d", num1 != num2);
}